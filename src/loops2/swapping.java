package loops2;

public class swapping {

	public static void main(String[] args) {
		
		int a=20;
		int b=10;
		int c;
		System.out.println("before sqapping :");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("After swapping :");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		

	}

}
