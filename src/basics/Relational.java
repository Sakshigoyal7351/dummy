package basics;

public class Relational {

	public static void main(String[] args) {
		
		int a=10;
		int b=10;
		
		
		System.out.println(a>b); //20>10
		System.out.println(a<b); //20<10
		
		System.out.println(a>=b);//(a>b)(a==b)//(20>10)(20==10)
		System.out.println(a<=b); //(a<b)(a==b) // 
		
		System.out.println(a!=b);

	}

}
